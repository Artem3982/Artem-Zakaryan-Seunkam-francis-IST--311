fahrenheit_values = [32, 50, 68, 86, 104]

kelvin_values = []
for F in fahrenheit_values:
    K = (F - 32) * 5 / 9 + 273.15
    kelvin_values.append(K)
    print(f"Температура в Фаренгейтах: {F}°F, в Кельвинах: {K:.2f} K")

for K in kelvin_values:
    if K % 2 == 0:
        print(f"{K:.2f} K является четным числом.")
    else:
        print(f"{K:.2f} K является нечетным числом.")

