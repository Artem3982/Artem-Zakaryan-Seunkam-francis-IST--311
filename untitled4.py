factorial_value = math.factorial(journal_number)
print(f"Факториал числа {journal_number}: {factorial_value}")

phone_memory = 256

def gcd(a, b):
    while b:
        a, b = b, a % b
    return a

greatest_divisor = gcd(journal_number, phone_memory)
print(f"Наибольший делитель для чисел {journal_number} и {phone_memory}: {greatest_divisor}")

